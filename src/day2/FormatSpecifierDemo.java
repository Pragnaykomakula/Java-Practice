package day2;

public class FormatSpecifierDemo {
    public static void main(String[] args) {

        String  name = "Pragnay Komakula";
        System.out.printf("hello my name is %s",name);

        int num1 = 900000;
        double num2 = 100.0023;
        double num3 = 86.253;

        System.out.printf("\n%d",num1);
        System.out.printf("\n%f",num2);
        System.out.printf("\n%f",num3);


    }
}
